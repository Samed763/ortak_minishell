int	is_token(int c)
{
	return (c == '|' || c == '<' || c == '>');
}

int	is_valid_to_expand(int c)
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_'
		|| c == '?');
}
